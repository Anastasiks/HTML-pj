let t = 0;
function f1(id){
  a = document.getElementById(id);
  t ++;
  if(t % 2 == 1){
    document.getElementById("p1").style.display="block";
    a.style.backgroundColor = "grey"}
  else{
    document.getElementById("p1").style.display="none";
    a.style.backgroundColor = "red"}
}
